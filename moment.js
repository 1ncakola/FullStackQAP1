// This code imports the moment library, gets the current date and time, formats it in a readable format, and prints it to the console.


// Import the moment module
const moment = require('moment');

// Use moment to get the current date and time
const now = moment();

// Output the current date and time to the console
console.log('Current Date and Time:', now.format('YYYY-MM-DD HH:mm:ss'));



